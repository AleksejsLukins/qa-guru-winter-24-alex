package model;

public class Jewlery {

    private String jewleryMetal;
    private String jewleryStyle;
    private String jewleryOrigi ;
    private int jewleryCost;
    private int jewleryNumbersOfItemSold;

    public String getJewleryMetal() {
        return jewleryMetal;
    }

    public void setJewleryMetal(String jewleryMetal) {
        this.jewleryMetal = jewleryMetal;
    }

    public String getJewleryStyle() {
        return jewleryStyle;
    }

    public void setJewleryStyle(String jewleryStyle) {
        this.jewleryStyle = jewleryStyle;
    }

    public String getJewleryOrigi() {
        return jewleryOrigi;
    }

    public void setJewleryOrigi(String jewleryOrigi) {
        this.jewleryOrigi = jewleryOrigi;
    }

    public int getJewleryCost() {
        return jewleryCost;
    }

    public void setJewleryCost(int jewleryCost) {
        this.jewleryCost = jewleryCost;
    }

    public int getJewleryNumbersOfItemSold() {
        return jewleryNumbersOfItemSold;
    }

    public void setJewleryNumbersOfItemSold(int jewleryNumbersOfItemSold) {
        this.jewleryNumbersOfItemSold = jewleryNumbersOfItemSold;
    }


}
