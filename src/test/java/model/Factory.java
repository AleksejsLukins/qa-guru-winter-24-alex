package model;

public class Factory {

    private String factoryCity;
    private String factoryName;
    private String factoryOriginCountry;
    private int factoryHarmnessLevel;
    private int factoryNumberOfWorkers;

    public String getFactoryCity() {
        return factoryCity;
    }

    public void setFactoryCity(String factoryCity) {
        this.factoryCity = factoryCity;
    }

    public String getFactoryName() {
        return factoryName;
    }

    public void setFactoryName(String factoryName) {
        this.factoryName = factoryName;
    }

    public String getFactoryOriginCountry() {
        return factoryOriginCountry;
    }

    public void setFactoryOriginCountry(String factoryOriginCountry) {
        this.factoryOriginCountry = factoryOriginCountry;
    }

    public int getFactoryHarmnessLevel() {
        return factoryHarmnessLevel;
    }

    public void setFactoryHarmnessLevel(int factoryHarmnessLevel) {
        this.factoryHarmnessLevel = factoryHarmnessLevel;
    }

    public int getFactoryNumberOfWorkers() {
        return factoryNumberOfWorkers;
    }

    public void setFactoryNumberOfWorkers(int factoryNumberOfWorkers) {
        this.factoryNumberOfWorkers = factoryNumberOfWorkers;
    }

}
